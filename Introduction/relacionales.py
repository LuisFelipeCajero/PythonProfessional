#Operadores relacionales

numero_uno = 10
numero_dos = 15

resultado = numero_uno < numero_dos
print(resultado)
resultado = numero_uno > numero_dos
print(resultado)
resultado = numero_uno <= numero_dos
print(resultado)
resultado = numero_uno >= numero_dos
print(resultado)
resultado = numero_uno == numero_dos
print(resultado)
resultado = numero_uno != numero_dos
print(resultado)
